import numpy as np

def пересчитать_скорости(скорости, ориентация):
    # Преобразование ориентации в матрицу поворота
    курс, крен, тангаж = ориентация
    матрица_поворота = np.array([[np.cos(крен)*np.cos(тангаж), np.cos(тангаж)*np.sin(крен), -np.sin(тангаж)],
                                  [np.cos(крен)*np.sin(тангаж)*np.sin(курс)-np.cos(курс)*np.sin(крен), np.cos(курс)*np.cos(крен)+np.sin(курс)*np.sin(тангаж)*np.sin(крен), np.cos(тангаж)*np.sin(курс)],
                                  [np.sin(курс)*np.sin(крен)+np.cos(курс)*np.cos(крен)*np.sin(тангаж), np.cos(курс)*np.sin(курс)*np.sin(крен)-np.cos(крен)*np.sin(тангаж), np.cos(курс)*np.cos(тангаж)]])

    # Перемножение матрицы поворота и скоростей для пересчета
    новые_скорости = np.dot(матрица_поворота, скорости)

    return новые_скорости

# Пример использования функции
скорости = np.array([0, 10, 10])  # Пример скоростей по осям за прошлый такт
ориентация = np.array([0, 0, np.pi])  # Пример ориентации летательного аппарата

новые_скорости = пересчитать_скорости(скорости, ориентация)
print(новые_скорости)